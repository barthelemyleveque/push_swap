#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	while (s1[i])
		i++;
	while (s2[c])
	{
		if (&s1[i] == &s2[i])
			return ((void*)0);
		s1[i] = s2[c];
		i++;
		c++;
	}
	s1[i] = '\0';
	return (s1);
}
